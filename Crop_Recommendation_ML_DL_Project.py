print("Initializing base models (Random Forest, Naive Bayes, Gradient Boosting)...")
rf = RandomForestClassifier(random_state=42)
nb = GaussianNB()
gb = GradientBoostingClassifier(random_state=42)

# 4. Create the Ensemble Model (Soft Voting)
# 'Soft' voting averages the probabilities of all 3 models to make the smartest choice
print("Creating the Ensemble Model...")
ensemble_model = VotingClassifier(
    estimators=[('rf', rf), ('nb', nb), ('gb', gb)],
    voting='soft'
)

# 5. Train the Ensemble Model
print("Training the Ensemble...")
ensemble_model.fit(X_train, y_train)

