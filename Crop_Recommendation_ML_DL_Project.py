import pandas as pd
import ipywidgets as widgets
from IPython.display import display, clear_output
import shap

# Initialize a small background dataset for SHAP to use (makes it fast)
# Assumes X_train and nb_model are already loaded in your Colab session
background = shap.sample(X_train, 50)

# --- 1. Create the Input Boxes ---
style = {'description_width': 'initial'}
n_input = widgets.FloatText(value=80, description='Nitrogen (N):', style=style)
p_input = widgets.FloatText(value=28, description='Phosphorous (P):', style=style)
k_input = widgets.FloatText(value=44, description='Potassium (K):', style=style)
temp_input = widgets.FloatText(value=25.0, description='Temperature (°C):', style=style)
hum_input = widgets.FloatText(value=80.0, description='Humidity (%):', style=style)
ph_input = widgets.FloatText(value=6.5, description='pH Level:', style=style)
rain_input = widgets.FloatText(value=220.0, description='Rainfall (mm):', style=style)

# --- 2. Create a Predict Button and Output Area ---
predict_button = widgets.Button(
    description='Get Recommendation & Explain',
    button_style='success', # Makes the button green
    tooltip='Click to predict crop and explain'
)
output_area = widgets.Output()

# --- 3. Define what happens when you click the button ---
def on_button_clicked(b):
    with output_area:
        clear_output(wait=True) # Clear the previous result

        # Gather the values from the boxes
        user_data = pd.DataFrame({
            'N': [n_input.value],
            'P': [p_input.value],
            'K': [k_input.value],
            'temperature': [temp_input.value],
            'humidity': [hum_input.value],
            'ph': [ph_input.value],
            'rainfall': [rain_input.value]
        })

        # Predict using your Naive Bayes model (nb_model)
        predicted_crop = nb_model.predict(user_data)[0]
        confidence = max(nb_model.predict_proba(user_data)[0]) * 100

        # Print the result
        print("========================================")
        print(f"🌾 RECOMMENDED CROP : {predicted_crop.upper()}")
        print(f"📊 AI CONFIDENCE    : {confidence:.2f}%")
        print("========================================\n")


        # --- EXPLAINABLE AI LOGIC (Text-based) ---
        class_idx = list(nb_model.classes_).index(predicted_crop)

        # Create a function that isolates the probability of the chosen crop
        def predict_fn(X):
            return nb_model.predict_proba(X)[:, class_idx]

        # Run SHAP explainer
        explainer = shap.KernelExplainer(predict_fn, background)
        shap_vals = explainer.shap_values(user_data, silent=True)[0]

        # Pair each feature with its SHAP impact score
        feature_impacts = list(zip(user_data.columns, shap_vals, user_data.iloc[0]))

        # Sort by the highest positive impact (the features that convinced the AI the most)
        feature_impacts.sort(key=lambda x: x[1], reverse=True)

        # Grab the top 2 features that drove the decision
        top_features = [f"{feat} ({val})" for feat, impact, val in feature_impacts if impact > 0][:2]

        # Generate the natural language explanation
        explanation_text = f"The AI highly recommends {predicted_crop.capitalize()}."
        if top_features:
            explanation_text += f" This decision was primarily driven by your specific levels of {' and '.join(top_features)}."

        # Print the explanation
        print("💡 EXPLANATION:")
        print(explanation_text)
        print("\n")

# Attach the click event to the button
predict_button.on_click(on_button_clicked)

